# Addition
num1 = 10
num2 = 5
result_add = num1 + num2
print("Addition:", result_add)

# Subtraction
result_sub = num1 - num2
print("Subtraction:", result_sub)

# Multiplication
result_mul = num1 * num2
print("Multiplication:", result_mul)

# Division
result_div = num1 / num2
print("Division:", result_div)